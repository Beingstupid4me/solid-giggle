import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";

const root = resolve(process.cwd());
const inputPath = resolve(root, "design-system/tokens/design-tokens.json");
const cssOutputPath = resolve(root, "src/design-system/tokens.css");
const jsOutputPath = resolve(root, "src/design-system/tokens.generated.js");

function ensureDir(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

function loadTokens() {
  const raw = readFileSync(inputPath, "utf8");
  return JSON.parse(raw);
}

function toKebabCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[\s_]+/g, "-")
    .toLowerCase();
}

function flattenObject(input, prefix = "") {
  const output = {};
  for (const [key, value] of Object.entries(input)) {
    if (key.startsWith("$")) continue;

    const nextKey = prefix ? `${prefix}-${toKebabCase(key)}` : toKebabCase(key);

    if (value && typeof value === "object" && !Array.isArray(value)) {
      Object.assign(output, flattenObject(value, nextKey));
    } else {
      output[nextKey] = value;
    }
  }
  return output;
}

function buildCss(tokens) {
  const tokenPayload = { ...tokens };
  delete tokenPayload.meta;

  const flat = flattenObject(tokenPayload);

  const lines = [
    "/* Auto-generated by scripts/build-design-tokens.mjs. Do not edit manually. */",
    ":root {"
  ];

  for (const [tokenName, tokenValue] of Object.entries(flat)) {
    if (typeof tokenValue === "string" || typeof tokenValue === "number") {
      lines.push(`  --ds-${tokenName}: ${tokenValue};`);
    }
  }

  lines.push("}");
  lines.push("");

  return lines.join("\n");
}

function buildJs(tokens) {
  return [
    "// Auto-generated by scripts/build-design-tokens.mjs. Do not edit manually.",
    `export const designTokens = ${JSON.stringify(tokens, null, 2)};`,
    ""
  ].join("\n");
}

function main() {
  const tokens = loadTokens();
  const css = buildCss(tokens);
  const js = buildJs(tokens);

  ensureDir(cssOutputPath);
  ensureDir(jsOutputPath);

  writeFileSync(cssOutputPath, css, "utf8");
  writeFileSync(jsOutputPath, js, "utf8");

  console.log("Design tokens built:");
  console.log(`- ${cssOutputPath}`);
  console.log(`- ${jsOutputPath}`);
}

main();
